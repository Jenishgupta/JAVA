// Java program to add two numbers and display the result
class Addition 
{
	public static void main(String[] args) 
	{
		// declaring and initializing two integer variables
		int a = 10;
		int b = 20;
		
		// performing addition
		int c = a + b;
		
		// displaying the result
		System.out.println("Result: " + c);
	}
}
