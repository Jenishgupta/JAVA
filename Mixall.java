class Mixall {
	public static void main(String[] args) {
		int [] ab = {3,4,5,6};
		double [] da = {45.7,66.8,39.2};
		String [] sa = {"JEN", "JOHN", "MARCOS"};
		
		for (int value : ab) {
			System.out.println("ab:-" +value);
		}
		for (double value : da) {
			System.out.println("da:-" +value);
		}
		for (String value : sa) {
			System.out.println("sa:-" +value);
		}
		
	}
}
